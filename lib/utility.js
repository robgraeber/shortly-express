// assumes usernames are unique

exports.createSession = function (userId, response) {
  var sessionId = Math.random() * 10000;
  var session = {};
  session.userId = userId;
  // session.loginTime = Date.now();
  sessionCache[sessionId] = session;
  response.cookie('sessionId', sessionId, { maxAge: 60000, httpOnly: true });
};

exports.getSession = function (sessionId) {
  return sessionCache[sessionId];
  // either session id or undefined
};
exports.removeSession = function(sessionId){
  console.log("Removed Session:", sessionCache[sessionId]);
  delete sessionCache[sessionId];
}
